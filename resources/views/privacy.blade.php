カスタマーごとにカスタマイズされたサービスを提供するにあたり、カスタマーが利便性を考慮しクッキーを使用することがあります。このクッキーは、カスタマーがカスタマイズされたページへアクセスしたとき、またはログインするときに設定されます。
5.個人情報の管理及び保護
当社は、カスタマーの個人情報について、次のとおり、安全管理措置を講じます。①個人情報の適正な取扱いの確保のため、本プライバシーポリシーを策定し掲載します。②個人情報の取扱いに関する責任者を設置するとともに、個人情報を取り扱う従業員および当該従業員が取り扱う個人情報の範囲を明確化し、個人情報保護関連法令や社内規程に違反している事実又は兆候を把握した場合の責任者への報告連絡体制等の組織的安全管理措置を整備します。③個人情報の取扱いに関する留意事項について、従業員に定期的な研修を実施します。④個人情報を取り扱う区域において、従業員の入退室管理および持ち込む機器等の制限を行うとともに、個人情報に係るデータベースなどへのアクセスについて、アクセス権限を有する者を限定し、社内において、個人情報に関して不正な取扱いがされないように厳重に管理します。⑤外部からの不正アクセス又は不正ソフトウェア、紛失、破壊、改ざん、なりすまし、盗聴などの危険に対しては、対策システムの導入、ＳＳＬ（Secure Socket Layer）などの適切かつ合理的なレベルの技術的安全管理措置を実施します。当社は、メールや郵便物（賞品等を含む）の処理・送付、カスタマーサポートの提供、配送の手配、統計データの作成等の業務を社外のビジネスパートナー（以下「ビジネスパートナー」という）に委託する場合があり、このような業務の委託に際し、個人情報をビジネスパートナーに預託する場合があります。この場合、当社は、個人情報を適切に管理できると認められるビジネスパートナーを選定し、業務委託契約等において、個人情報の管理、再委託の禁止、損害賠償義務等、カスタマーの個人情報の漏洩がないよう必要な事項をビジネスパートナーと書面による取り決めを行っております。また、ビジネスパートナーに対する必要かつ適切な監督を行います。ビジネスパートナーは、契約において個人情報を厳重に管理するとともに、秘密に保持する義務を負っており、当社の指定した範囲を超えて個人情報を取り扱うことを禁止されています。
6.個人情報の第三者への開示
当社は、原則として、カスタマーの個人情報を第三者に開示することはありません。但し、以下のような場合、その必要性の有無を十分検討した上で、個人情報を開示することがあります。
 	人の生命、身体、財産の保護のために必要がある場合であって、カスタマー本人の同意を得ることが困難である場合
 	公衆衛生の向上又は児童の健全な育成の推進のために特に必要がある場合であって、カスタマー本人の同意を得ることが困難である場合
 	国の機関若しくは地方公共団体又はその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、カスタマー本人の同意を得ることにより当該事務の遂行に支障を及ぼすおそれがある場合
 	その他法令により当社に個人情報の開示又は提供義務が定められている場合
 	カスタマーが本ウェブサイトを利用したことにより、関係する第三者（広告主・運送業者等）から当該第三者がカスタマーにサービス等を提供する上で必要な情報の提供を求められた場合
 	当社が営業の全部又は一部を第三者に譲渡するか、あるいは分社化する場合で、当社から営業譲渡を受けた第三者又は分社した会社に譲渡する場合
 	カスタマー本人を特定・識別することのできない情報
 	その他カスタマーへのサービス提供のために必要であると当社が合理的に判断した場合
