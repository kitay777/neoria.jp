プライバシーポリシー
株式会社ネオリア（以下「当社」といいます）は、当社が運営するウェブサイト「ネオリア」及び関連サイト（以下、あわせて「本ウェブサイト」といいます。）について、利用者（以下「カスタマー」といいます。）の個人情報の保護及びカスタマーが本ウェブサイトを安心して利用しうる体制の構築を目的として、『ネオリア プライバシーポリシー』（以下、単に「プライバシーポリシー」といいます。）を定め、プライバシーポリシーに基づきカスタマーより収集した個人情報を取扱うものとします。
1.プライバシーポリシーの適用範囲
プライバシーポリシーは、本ウェブサイトにおいて当社がカスタマーより収集する個人情報の取扱いに関して適用されます。本ウェブサイトにおいて、当社の広告主等が主体となって、その掲載した広告の中でアンケート等を実施し、カスタマーの個人情報を収集する場合があります。この場合は、当社は、メディアとして広告枠を広告主に提供しているにすぎず、広告主が個人情報の収集・利用・保有・管理等の主体となります。広告主は、独自のプライバシーポリシー等を定め、当該プライバシーポリシー等に従い個人情報を取り扱う独立した事業者であり、このような形で収集された個人情報については、当社は保有を行わず、従ってプライバシーポリシーは適用されません。
2.個人情報の定義
プライバシーポリシーにおいて個人情報とは、個人情報保護法に定める個人情報（生存する個人に関する情報であり、特定の個人を識別可能なものを言い、氏名、住所、電話番号等を含みます。）並びに特定の個人と結びついて使用されるメールアドレス、パスワード、クレジットカードなどの情報及びそれらと一体となった購入取引に関する情報、趣味、家族構成、年齢その他の個人に関する属性情報をいうものとします。
3.統計データの利用
当社は、本ウェブサイトにおいて、カスタマーが本ウェブサイトの提供するサービスを利用する場合、本ウェブサイトのモニターとして登録する場合、本ウェブサイトが行う何らか情報提供を受ける旨の意思を当社に対して表示した場合、本ウェブサイトにおいて当社が実施するアンケートに協力する場合や懸賞等に応募する場合、又は本ウェブサイトにおいて当社の販売する商品を購入する場合（メンバーへの登録を含みます。）等に個人情報を収集します。当社が本ウェブサイトにおいてカスタマーの個人情報を収集する機会は、本ウェブサイトの更新等によって変更されます。従って、前述のケースは、当社が本ウェブサイトにおいてカスタマーの個人情報を収集する機会の一部を例示したものであり、これらに限られるものではありません。当社がカスタマーに登録をお願いする個人情報は、カスタマーが本ウェブサイトのサービスを利用する上で必要となるものに限られています。当社がカスタマーから収集する個人情報は、本ウェブサイト内で当社が提供するサービス等によって異なります。サービスによっては、カスタマーのメールアドレス、住所、氏名、電話番号等の個人情報を収集する場合もあれば、年齢、性別、嗜好、興味などの統計的な情報を収集する場合もあります。また、カスタマーが当社の販売する商品を購入する場合には、クレジットカード番号等の登録を依頼する場合もあります。
4.個人情報の利用
当社は、カスタマーから収集した個人情報を主として次の目的で利用します。なお、本ウェブサイトにおいて当社が提供するサービス、商品をよりよいものとするために、これらの目的以外の目的で、カスタマーより同意を得た範囲において個人情報を利用する場合があります。
 	当社サービスを提供するため
 	当社サービスを安心・安全にご利用いただける環境整備のため
 	当社サービスの運営・管理のため
 	当社サービスに関するご案内、お問い合せ等への対応のため
 	当社サービスに関する各種ご案内、メンテナンス情報、その他の重要なご連絡等、
 	当社、その他当社サービスについての調査・データ集積、改善、研究開発のため
 	当社及び第三者の商品・サービスなどのご案内を送信・送付するため
 	当社とお客様の間での必要な連絡を行うため
 	当社及び第三者の商品・サービス代金の決済のため
 	当社及び第三者の料金請求、課金計算のため
