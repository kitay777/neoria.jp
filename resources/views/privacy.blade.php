9.クッキーに関して
クッキー (Cookie) とは、ウェブサイトが記録を保持する目的で、利用者のコンピュータのハードディスクに送付する小さなテキストファイルです。クッキーを利用すると、利用者の特定のサイトに対する好みに関する情報を記憶して、ウェブの利用をより有益なものにできます。クッキーの使用は業界の標準となっており、多くのサイトで、利用者に有益な機能を提供する目的でクッキーが使用されています。クッキーにより、利用者の使用するコンピュータは特定されますが、利用者個人を特定できるわけではありません。当社は、本ウェブサイトにおいて、以下のような場合にクッキーを使用することがあります。カスタマーごとにカスタマイズされたサービスを提供するにあたり、カスタマーが利便性を考慮しクッキーを使用することがあります。このクッキーは、カスタマーがカスタマイズされたページへアクセスしたとき、またはログインするときに設定されます。 本ウェブサイトの利用者数を計るために使用する場合があります。掲載された広告についてもクッキーを使用する場合があります。
